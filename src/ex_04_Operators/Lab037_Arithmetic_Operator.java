package ex_04_Operators;

public class Lab037_Arithmetic_Operator {
    public static void main(String[] args) {
        int a =10;
        int b = 3;
        float c = 0.3f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println("a+b"); // string
        System.out.println(a+b);

    }
}
